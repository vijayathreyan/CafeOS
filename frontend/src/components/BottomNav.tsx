export default function BottomNav() {
  const { t } = useTranslation()
  const { user } = useAuth()
  if (!user) return null

  const navClass = ({ isActive }: { isActive: boolean }) =>
    `flex flex-col items-center justify-center gap-1 flex-1 min-h-tap py-2 transition-colors ${
      isActive ? 'text-primary' : 'text-text-secondary'
    }`

  return (
    <nav className="bg-surface border-t border-border safe-bottom">
      <div className="flex items-stretch">
        <NavLink to="/" end className={navClass}>
          <HomeIcon />
          <span className="text-xs font-medium">{t('nav.dashboard')}</span>
        </NavLink>

        {(user.role === 'staff' || user.role === 'supervisor') && (
          <NavLink to="/shift" className={navClass}>
            <ShiftIcon />
            <span className="text-xs font-medium">{t('nav.shift')}</span>
          </NavLink>
        )}

        {user.role === 'owner' && (
          <NavLink to="/users" className={navClass}>
            <UsersIcon />
            <span className="text-xs font-medium">{t('employees.title')}</span>
          </NavLink>
        )}

        {user.role === 'owner' && (
          <NavLink to="/reports" className={navClass}>
            <ReportIcon />
            <span className="text-xs font-medium">{t('nav.reports')}</span>
          </NavLink>
        )}

        <NavLink to="/tasks" className={navClass}>
          <TaskIcon />
          <span className="text-xs font-medium">{t('nav.tasks')}</span>
        </NavLink>

        {user.role === 'owner' && (
          <NavLink to="/settings" className={navClass}>
            <SettingsIcon />
            <span className="text-xs font-medium">{t('nav.settings')}</span>
          </NavLink>
        )}
      </div>
    </nav>
  )
}
